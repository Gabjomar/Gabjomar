"""
Tipo de dado bool (boolean)
Ao questionar algo em um programa, só existem duas respostas possíveis: "sim" (True) ou "não" (False).
Existem vários operadores para "questionar". Dentre eles o "==", que é um operador lógico que questiona se um valor é igual ao outro.
"""
print(10 == 2); # False
print(type(10.2) == int) # False
print(type(10) == int) # True
print(type(10 == 2)) # bool
print(type(False)) # bool
print(type(True)) # bool
