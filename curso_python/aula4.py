'''
Tipos int e float
---
int -> Número inteiro.
O tipo int representa qualquer número positivo ou negativo. 
O int sem sinal é considerado positivo.

---
float -> Número com ponto flutuante.
O tipo float representa qualquer número positivo ou negativo com ponto flutuante.
Float sem sinal é considerado positivo.
Na programação, o separador decimal é sempre ".".
'''

print(3.4); # 1 número float.
print(3,4); # 2 números int.

# A função type mostra o tipo que o Python inferiu ao valor.
print(type(3.4),type(3),type(4),type("3.4"),type(0));

# Por convenção, toda função começa com letra minúscula e toda classe começa com letra maiúscula.
# "Tudo no Python é um objeto".