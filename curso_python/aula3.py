'''
Python -> Linguagem de programação.
Tipo de tipagem -> Dinâmica (já sabe qual é o tipo de informação passada para ele) / Forte ().
str -> string -> texto.
Strings são textos que estão dentro de aspas.
'''

# Por ora, strings podem ser inseridas em aspas simples ou aspas duplas.

print('Hello, world!');
print("Hello, world!");
print("Hello, \"world\"!");
print('Hello, \"world\"!');
print('Hello, \'world\'!');
print(r"Hello, \"world\"!"); # É possível, porém muito trabalhoso.
print('Hello, "world"!'); # Forma mais simples de obter o mesmo resultado.
